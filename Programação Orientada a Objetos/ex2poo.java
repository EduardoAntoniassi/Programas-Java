public class ex2poo {
    public static void main(String[] args) {
        
        // declaracao e inicializacao de variaveis
        int x = 10;
        int y = 3;

        // varias operacoes com as variaveis
        System.out.println("X = " + x);
        System.out.println("\nY = " + y);
        System.out.println("\n-X = " + (-x));
        System.out.println("\nX / Y = " + (x / y));
        System.out.println("\nResto de X por Y = " + (x % y)); // resulta 1
        System.out.println("\nInteiro de X por Y = " + (int) (x / y)); // resulta 3
        System.out.println("\nX + 1 = " + (++x)); // resulta 11
    
    }
}
